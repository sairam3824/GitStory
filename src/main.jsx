function LoadingPage() {
  const { username } = useParams();
  const navigate = useNavigate();
  const [active, setActive] = useState(0);
  const steps = useMemo(
    () => [
      ["Fetching your repos...", 1500],
      ["Analyzing your journey...", 1500],
      ["Crafting your story...", 2000],
      ["Designing your page...", 1000]
    ],
    []
  );

  useEffect(() => {
    let total = 0;
    const timers = steps.map(([, delay], index) => {
      total += delay;
      return setTimeout(() => setActive(index + 1), total);
    });
    const done = setTimeout(() => navigate(`/story/${username}`), total + 250);
    return () => [...timers, done].forEach(clearTimeout);
  }, [navigate, steps, username]);

  return (
    <main className="hero-grid flex min-h-screen items-center justify-center bg-github px-5 text-white">
      <div className="w-full max-w-xl text-center">
        <div className="mx-auto mb-8 flex h-20 w-20 items-center justify-center rounded-full border border-orange/40 bg-orange/10 shadow-glow">
          <Loader2 className="animate-spin text-orange" size={34} />
        </div>
        <h1 className="text-4xl font-black">Your GitHub is being turned into a legend.</h1>
        <div className="mt-10 space-y-4 text-left">
          {steps.map(([label], index) => (
            <div
              key={label}
              className={cx(
                "flex items-center gap-3 rounded-lg border p-4 transition",
                index < active
                  ? "border-orange/50 bg-orange/10 text-white"
                  : index === active
                    ? "border-white/15 bg-white/[0.06] text-slate-200"
                    : "border-white/10 bg-white/[0.03] text-slate-500"
              )}
            >
              {index < active ? <CheckCircle2 className="text-orange" size={20} /> : <Loader2 className={index === active ? "animate-spin text-orange" : ""} size={20} />}
              <span className="font-medium">{label}</span>
            </div>
          ))}
        </div>
      </div>
    </main>
  );
}

