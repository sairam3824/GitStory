function useReveal(refreshKey) {
  useEffect(() => {
    const items = document.querySelectorAll("[data-reveal]");
    const observer = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) entry.target.classList.add("is-visible");
        });
      },
      { threshold: 0.18 }
    );
    items.forEach((item) => observer.observe(item));
    return () => observer.disconnect();
  }, [refreshKey]);
}


function timeAgo(date) {
  const seconds = Math.max(1, Math.floor((Date.now() - new Date(date).getTime()) / 1000));
  const units = [
    ["y", 31536000],
    ["mo", 2592000],
    ["d", 86400],
    ["h", 3600],
    ["m", 60]
  ];
  for (const [label, size] of units) {
    const value = Math.floor(seconds / size);
    if (value >= 1) return `${value}${label} ago`;
  }
  return "just now";
}

function updateMeta(storyData) {
  const profile = storyData.github.profile;
  const title = `${profile.name || profile.login}'s GitStory`;
  const description = storyData.story.headline;
  document.title = title;

  const tags = {
    "og:title": title,
    "og:description": description,
    "og:image": profile.avatar_url
  };

  for (const [property, content] of Object.entries(tags)) {
    let tag = document.querySelector(`meta[property="${property}"]`);
    if (!tag) {
      tag = document.createElement("meta");
      tag.setAttribute("property", property);
      document.head.appendChild(tag);
    }
    tag.setAttribute("content", content);
  }
}

